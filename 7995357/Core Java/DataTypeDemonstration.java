public class DataTypeDemonstration {
    public static void main(String[] args) {
        byte age = 25;
        short employeeCount = 5000;
        int salary = 700000;
        long companyRevenue = 9876543210L;
        float percentage = 85.5f; 
        double pi = 3.141592653589793;
        char grade = 'A';
        boolean isSelected = true;
        System.out.println(" Java Primitive Data Types ");

        

        System.out.println( "byte    : " + age);
        System.out.println("short   : " + employeeCount);
        System.out.println("int     : "+salary);
        System.out.println("long    : "+ companyRevenue);
        System.out.println("float   : "+ percentage);
        System.out.println("double  : "+ pi);
        System.out.println("char    : "+ grade);
        System.out.println("boolean : "+ isSelected);

    }
}